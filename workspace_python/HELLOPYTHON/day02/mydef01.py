# JS function과 같은 기능 def
def add(a, b):
    return a+b

#minus, mul, div, mymode

def minus(a, b):
    return a-b

def mul(a,b):
    return a*b

def div(a,b):
    return a/b

def mymode(a,b):
    return a%b

sum = add(1,5)
print(sum)

minu = minus(5,2)
print(minu)

multi = mul(4,6)
print(multi)

divide = div(8,4)
print(divide)

modi = mymode(7, 3)
print(modi)